class ExampleLoops 
{
	public static void main(String[] args) 
	{


//   series from A  to Z
		for (char ch = 'A' ; ch <= 'Z' ; ch++ )
		{
			System.out.print(" " + ch);
		}
    
System.out.println();

//   series from a to z
		for (char i = 'a' ; i <= 'z' ; i++)
		{
			System.out.print(" " + i + " " );
		}


System.out.println();


//   Digit 0 to 9

       for ( char digit = '0' ; digit <= '9'  ; digit++ )
       {
		   System.out.print(" " + digit);
       }
		
	}
}
